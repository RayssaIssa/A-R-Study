                <h4 class="tema">Partilha da Ásia</h4>
                <p class="texto">
                A expansão européia enfrentou forte resistência em países como China e Japão.
                Entretanto, o poderio militar dos europeus, aliado ao dos EUA, foi vencendo,
                gradativamente, a resistência dos diversos povos asiáticos. A partir de 1867, o próprio
                Japão entrou numa fase de rápida modernização de sua estrutura socioeconômica,
                dando início ao processo que iria levar este país a ser uma potência imperialista.<br><br>

                -> <b>Índia</b>: Desde 1763 este país era um protetorado inglês, sendo que a Inglaterra
                ocupava a maior parte dos cargos administrativos do país.<br>
                <b>Guerra dos Sipaios</b> – o nacionalismo indiano era alimentado pela crescente presença
                inglesa. Em 1857 ocorreu a Guerra dos Sipaios, que lutou contra a dominação. Os
                sipaios eram membros da milícia nativa organizada pela Inglaterra para servir à Coroa.
                A rebelião começou nas unidades militares, contra os oficiais ingleses. Os revoltosos
                foram sufocados em 1859. Anos mais tarde, em 1876, a rainha Vitória seria coroada
                como imperatriz da Índia.<br><br>

                -> <b>China</b>: Em meados do século XIX a China representava um grande mercado
                consumidor. Isto atraiu as várias potências imperialistas para este país. Através de
                diversas guerras e conflitos a China acabou sendo subjugada.<br>
                <b>Guerra do Ópio (1841)</b> – O ópio era utilizado pelos chineses, até o século XVIII, como
                medicamento. Como os ingleses produziam grandes quantidades dessas drogas em
                seus domínios indianos, estes forçaram o mercado para exportar o ópio para a China,
                disseminando o vício entre os seus habitantes.<br>
                Em 1839 as autoridades chinesas obrigaram o representante britânico a jogar no mar
                mais de 20 mil caixas de ópio. Como os chineses se negaram a pagar a indenização
                pelos produtos ingleses, iniciou-se a Guerra do Ópio. Em 1842, a China foi obrigada a
                assinar o Tratado de Nanquim, pelo qual abria cinco de seus portos ao livre comércio,
                abolia o sistema fiscal e entregava Hong Kong à Inglaterra.<br>
                <b>Guerra dos Boxers (1900)</b> – Os chineses nacionalistas do grupo dos Boxers, ou os
                “Punhos Fechados” objetivavam libertar o país do jugo imperialista dos ingleses. Em
                1900 organizaram uma grande rebelião onde morreram mais de 200 estrangeiros. A
                represália foi feita por uma força expedicionária internacional composta por ingleses,
                franceses, alemães, russos, japoneses e norte-americanos.<br><br>

                -> <b>Japão</b> : No século XIX, o Japão era dominado por uma aristocracia apoiada numa
                classe de guerreiros profissionais, os Samurais. A estrutura era ainda Feudal. Apesar de
                os japoneses terem um imperador (micado), o comando político efetivo cabia ao
                Xogunato (comandante militar).<br>
                Os EUA Chegam ao Japão – Em 1854 uma esquadra norte-americana chegou ao
                Japão e forçou a abertura dos portos japoneses ao comércio mundial. Tinha inicio a
                europeização do país.<br>
                Devido a passividade do Xogunato quanto à entrada dos americanos, os grupos
                nacionalistas uniram-se em torno do imperador Mutsuhito, que promoveu a
                centralização política e inaugurou uma nova fase da história japonesa.<br>
                <b>Era Meiji</b> – a partir de 1868 o Japão começava a desenvolver sua indústria e se
                modernizava. O Japão deixava de ser um país feudal para se tornar um dos países
                Capitalistas imperialistas.<br><br>
